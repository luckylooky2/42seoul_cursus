#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	ch;

	ch = c;
	if (('A' <= ch && ch <= 'Z') || ('a' <= ch && ch <= 'z'))
		return (1);
	else
		return (0);
}
