#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	int		res;

	i = 0;
	res = 0;
	while (i < n)
	{
		res = ((const unsigned char *)s1)[i] - ((const unsigned char *)s2)[i];
		if (res != 0)
			break ;
		i++;
	}
	return (res);
}
